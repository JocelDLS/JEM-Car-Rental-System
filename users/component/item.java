    public void setData(ModelItem data) {
        this.data = data;

        if (data.getImage() != null) {
            lblImage.setImage(data.getImage());
        } else {
            lblImage.setImage(new ImageIcon("path_to_placeholder_image.png"));
        }

        lblName.setText(data.getFullName());

        lblTransmission.setText(data.getTransmission() != null ? "Transmission: " + data.getTransmission() : "Unknown");

        DecimalFormat df = new DecimalFormat("Price: â‚±#,##0.00");
        lblPrice.setText(df.format(data.getPrice_per_day()));
        lblVehicleType.setText( data.getVehicle_type() != null ? "Vehicle Type: " + data.getVehicle_type() : "Unknown");
        lblSeatingCapacity.setText("Seating Capacity: " + data.getSeating_capacity());
    }
    public ModelItem getVehicle() {
        return this.data;
    }

    @Override
    public void paint(Graphics g) {
        Graphics2D g2 = (Graphics2D)g.create();
        g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
        g2.setColor(new Color(242,242,242));
        g2.fillRoundRect(0, 0, getWidth(), getHeight(), 20,20);
        if (selected) {
            g2.setColor(new Color(94,156,255));
            g2.drawRoundRect(0, 0, getWidth()-1, getHeight()-1, 20, 20);
        }
        g2.dispose();
        super.paint(g);
    }
    
    

